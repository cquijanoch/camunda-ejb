package org.unsa.mybatis.bean;

public class Rol {

	private Integer rolId;
	private String nombre;

	public Rol() {

	}

	public Integer getRolId() {
		return rolId;
	}

	public void setRolId(Integer rolId) {
		this.rolId = rolId;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

}
